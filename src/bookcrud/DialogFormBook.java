    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (this.id != null) {
                updateRecord();
                return;
            }
            
            Connection c = DB.getConnection();
            
            String query = "INSERT INTO tb_book(name, price, page) VALUES(?, ?, ?)";
            
            PreparedStatement pre = c.prepareStatement(query);
            pre.setString(1, txtName.getText());
            pre.setString(2, txtPrice.getText());
            pre.setString(3, txtPage.getText());
            
            if(pre.executeUpdate() != -1) {
                this.dispose();
            } 
               
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        if(id != null) {
            try {
                Connection c = DB.getConnection();
                String query = "SELECT  * FROM tb_book WHERE id=" + id;
                
                ResultSet rs = c.createStatement().executeQuery(query);
                
                rs.next();
                txtName.setText(rs.getString("name"));
                txtPrice.setText(rs.getString("price"));
                txtPage.setText(rs.getString("page"));
                rs.close();
                
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_formWindowOpened

