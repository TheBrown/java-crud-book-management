    private void updateRecord() {
    
        try {
            String q = "UPDATE tb_book SET name=?, price=?, page=? where id=?";
            Connection c = DB.getConnection();
            
            PreparedStatement pre = c.prepareStatement(q);
            pre.setString(1, txtName.getText());
            pre.setString(2, txtPrice.getText());
            pre.setString(3, txtPage.getText());
            pre.setString(4, this.id);
            
            if(pre.executeUpdate() != -1) {
                this.dispose();
            } 
        } catch (Exception e) {
        }
    }
